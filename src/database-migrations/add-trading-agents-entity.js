// src/database-migrations/add-trading-agents-entity.js
// Migration to add Trading Agents as first-class entities
// Following the pattern from Famous Investors

const db = require('../database');

function runMigration() {
  const database = db.getDatabase();

  console.log('🤖 Running trading agents entity migration...');

  database.exec('BEGIN TRANSACTION');

  try {
    // ============================================
    // TABLE 1: Trading Agents (Core Entity)
    // The autonomous trading agent definition
    // ============================================
    console.log('  Creating trading_agents table...');
    database.exec(`
      CREATE TABLE IF NOT EXISTS trading_agents (
        id INTEGER PRIMARY KEY AUTOINCREMENT,

        -- Identity
        name TEXT NOT NULL,
        description TEXT,
        strategy_type TEXT DEFAULT 'hybrid' CHECK (strategy_type IN ('technical', 'fundamental', 'sentiment', 'hybrid', 'custom')),

        -- Status
        is_active INTEGER DEFAULT 1,
        status TEXT DEFAULT 'idle' CHECK (status IN ('idle', 'running', 'paused', 'error')),
        last_scan_at DATETIME,
        next_scan_at DATETIME,
        error_message TEXT,

        -- Signal Weights (NULL means use defaults)
        technical_weight REAL,
        sentiment_weight REAL,
        insider_weight REAL,
        fundamental_weight REAL,
        alternative_weight REAL,
        valuation_weight REAL,
        thirteenf_weight REAL,
        earnings_weight REAL,
        value_quality_weight REAL,

        -- Thresholds
        min_confidence REAL DEFAULT 0.6,
        min_signal_score REAL DEFAULT 0.3,

        -- Risk Parameters
        max_position_size REAL DEFAULT 0.10,
        max_sector_exposure REAL DEFAULT 0.30,
        min_cash_reserve REAL DEFAULT 0.05,
        max_drawdown REAL DEFAULT 0.20,
        max_correlation REAL DEFAULT 0.70,
        max_daily_trades INTEGER DEFAULT 10,

        -- Regime Behavior
        regime_scaling_enabled INTEGER DEFAULT 1,
        vix_scaling_enabled INTEGER DEFAULT 1,
        vix_threshold REAL DEFAULT 25,
        pause_in_crisis INTEGER DEFAULT 1,

        -- Execution Settings
        auto_execute INTEGER DEFAULT 0,
        execution_threshold REAL DEFAULT 0.8,
        require_confirmation INTEGER DEFAULT 1,
        allowed_actions TEXT DEFAULT '["buy","sell"]',

        -- Feature Flags
        use_optimized_weights INTEGER DEFAULT 1,
        use_hmm_regime INTEGER DEFAULT 1,
        use_ml_combiner INTEGER DEFAULT 0,
        use_factor_exposure INTEGER DEFAULT 1,
        use_probabilistic_dcf INTEGER DEFAULT 1,
        apply_earnings_filter INTEGER DEFAULT 1,
        earnings_blackout_days INTEGER DEFAULT 7,

        -- Universe Configuration
        universe_type TEXT DEFAULT 'all' CHECK (universe_type IN ('all', 'watchlist', 'custom', 'sector')),
        universe_filter TEXT,

        -- Region for sentiment sources (US, EU, UK, or 'all')
        region TEXT DEFAULT 'US' CHECK (region IN ('US', 'EU', 'UK', 'all')),

        -- Performance Tracking (denormalized for fast queries)
        total_signals_generated INTEGER DEFAULT 0,
        total_trades_executed INTEGER DEFAULT 0,
        total_trades_won INTEGER DEFAULT 0,
        win_rate REAL,
        avg_return REAL,
        total_return REAL,
        sharpe_ratio REAL,
        max_drawdown_actual REAL,

        -- Follower count (for future social features)
        followers_count INTEGER DEFAULT 0,

        -- Metadata
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
        updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
      );
    `);

    console.log('  Creating trading_agents indexes...');
    database.exec(`
      CREATE INDEX IF NOT EXISTS idx_trading_agents_active ON trading_agents(is_active, status);
      CREATE INDEX IF NOT EXISTS idx_trading_agents_strategy ON trading_agents(strategy_type);
      CREATE INDEX IF NOT EXISTS idx_trading_agents_status ON trading_agents(status);
    `);

    // ============================================
    // TABLE 2: Agent Signals (Time Series Data)
    // Signals generated by agents over time
    // ============================================
    console.log('  Creating agent_signals table...');
    database.exec(`
      CREATE TABLE IF NOT EXISTS agent_signals (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        agent_id INTEGER NOT NULL,

        -- Signal Details
        symbol TEXT NOT NULL,
        company_id INTEGER,
        signal_date DATETIME NOT NULL,
        action TEXT NOT NULL CHECK (action IN ('strong_buy', 'buy', 'hold', 'sell', 'strong_sell')),

        -- Scores
        overall_score REAL NOT NULL,
        confidence REAL NOT NULL,
        raw_score REAL,

        -- Component Signals (JSON for flexibility)
        signals TEXT,

        -- Context at Signal Time
        regime TEXT,
        regime_confidence REAL,
        price_at_signal REAL,
        market_cap_at_signal REAL,
        sector TEXT,

        -- Recommended Position
        position_size_pct REAL,
        position_value REAL,
        suggested_shares INTEGER,

        -- Risk Check Results
        risk_approved INTEGER,
        risk_checks TEXT,
        risk_warnings TEXT,
        risk_blockers TEXT,

        -- Reasoning
        reasoning TEXT,

        -- Tracking Status
        status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'rejected', 'executed', 'expired', 'cancelled')),
        expires_at DATETIME,

        -- Execution Details (if executed)
        executed_at DATETIME,
        executed_price REAL,
        executed_shares INTEGER,
        executed_value REAL,
        portfolio_id INTEGER,

        -- Outcome Tracking (for performance)
        outcome_tracked INTEGER DEFAULT 0,
        exit_date DATETIME,
        exit_price REAL,
        actual_return REAL,
        holding_period_days INTEGER,

        created_at DATETIME DEFAULT CURRENT_TIMESTAMP,

        FOREIGN KEY (agent_id) REFERENCES trading_agents(id) ON DELETE CASCADE,
        FOREIGN KEY (company_id) REFERENCES companies(id),
        FOREIGN KEY (portfolio_id) REFERENCES portfolios(id) ON DELETE SET NULL
      );
    `);

    console.log('  Creating agent_signals indexes...');
    database.exec(`
      CREATE INDEX IF NOT EXISTS idx_agent_signals_lookup ON agent_signals(agent_id, signal_date DESC);
      CREATE INDEX IF NOT EXISTS idx_agent_signals_symbol ON agent_signals(symbol, signal_date DESC);
      CREATE INDEX IF NOT EXISTS idx_agent_signals_status ON agent_signals(agent_id, status);
      CREATE INDEX IF NOT EXISTS idx_agent_signals_action ON agent_signals(action);
      CREATE INDEX IF NOT EXISTS idx_agent_signals_pending ON agent_signals(status, agent_id) WHERE status = 'pending';
      CREATE INDEX IF NOT EXISTS idx_agent_signals_portfolio ON agent_signals(portfolio_id);
    `);

    // ============================================
    // TABLE 3: Agent Portfolios (Link Table)
    // Links agents to portfolios they manage
    // ============================================
    console.log('  Creating agent_portfolios table...');
    database.exec(`
      CREATE TABLE IF NOT EXISTS agent_portfolios (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        agent_id INTEGER NOT NULL,
        portfolio_id INTEGER NOT NULL,

        -- Configuration for this portfolio instance
        mode TEXT DEFAULT 'paper' CHECK (mode IN ('paper', 'live')),
        initial_capital REAL,

        -- Override settings for this specific portfolio (NULL = use agent defaults)
        max_position_size_override REAL,
        max_sector_exposure_override REAL,
        auto_execute_override INTEGER,

        -- Activation tracking
        is_active INTEGER DEFAULT 1,
        activated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
        deactivated_at DATETIME,

        -- Performance for this specific portfolio
        total_trades INTEGER DEFAULT 0,
        winning_trades INTEGER DEFAULT 0,
        total_pnl REAL DEFAULT 0,

        created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
        updated_at DATETIME DEFAULT CURRENT_TIMESTAMP,

        FOREIGN KEY (agent_id) REFERENCES trading_agents(id) ON DELETE CASCADE,
        FOREIGN KEY (portfolio_id) REFERENCES portfolios(id) ON DELETE CASCADE,
        UNIQUE(agent_id, portfolio_id)
      );
    `);

    console.log('  Creating agent_portfolios indexes...');
    database.exec(`
      CREATE INDEX IF NOT EXISTS idx_agent_portfolios_agent ON agent_portfolios(agent_id, is_active);
      CREATE INDEX IF NOT EXISTS idx_agent_portfolios_portfolio ON agent_portfolios(portfolio_id);
    `);

    // ============================================
    // TABLE 4: Agent Activity Log
    // Tracks all agent activities for audit trail
    // ============================================
    console.log('  Creating agent_activity_log table...');
    database.exec(`
      CREATE TABLE IF NOT EXISTS agent_activity_log (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        agent_id INTEGER NOT NULL,
        portfolio_id INTEGER,

        -- Activity details
        activity_type TEXT NOT NULL CHECK (activity_type IN (
          'scan_started', 'scan_completed', 'scan_failed',
          'signal_generated', 'signal_approved', 'signal_rejected', 'signal_expired',
          'trade_executed', 'trade_failed',
          'agent_started', 'agent_paused', 'agent_resumed', 'agent_error',
          'settings_updated', 'portfolio_attached', 'portfolio_detached'
        )),
        description TEXT,
        details TEXT,

        -- Related entities
        signal_id INTEGER,
        trade_id INTEGER,

        created_at DATETIME DEFAULT CURRENT_TIMESTAMP,

        FOREIGN KEY (agent_id) REFERENCES trading_agents(id) ON DELETE CASCADE,
        FOREIGN KEY (portfolio_id) REFERENCES portfolios(id) ON DELETE SET NULL,
        FOREIGN KEY (signal_id) REFERENCES agent_signals(id) ON DELETE SET NULL
      );
    `);

    console.log('  Creating agent_activity_log indexes...');
    database.exec(`
      CREATE INDEX IF NOT EXISTS idx_agent_activity_agent ON agent_activity_log(agent_id, created_at DESC);
      CREATE INDEX IF NOT EXISTS idx_agent_activity_type ON agent_activity_log(activity_type, created_at DESC);
      CREATE INDEX IF NOT EXISTS idx_agent_activity_portfolio ON agent_activity_log(portfolio_id, created_at DESC);
    `);

    // ============================================
    // ALTER portfolios table to add agent_id reference
    // ============================================
    console.log('  Adding agent_id column to portfolios table...');

    // Check if column exists first
    const columnsResult = database.prepare(`PRAGMA table_info(portfolios)`).all();
    const hasAgentId = columnsResult.some(col => col.name === 'agent_id');

    if (!hasAgentId) {
      database.exec(`
        ALTER TABLE portfolios ADD COLUMN agent_id INTEGER REFERENCES trading_agents(id);
      `);
      database.exec(`
        CREATE INDEX IF NOT EXISTS idx_portfolios_agent ON portfolios(agent_id);
      `);
      console.log('    Added agent_id column');
    } else {
      console.log('    agent_id column already exists, skipping');
    }

    database.exec('COMMIT');

    console.log('');
    console.log('🤖 Trading agents entity migration completed!');
    console.log('');
    console.log('Tables created/modified:');
    console.log('  - trading_agents (new entity table)');
    console.log('  - agent_signals (time series data)');
    console.log('  - agent_portfolios (link table)');
    console.log('  - agent_activity_log (audit trail)');
    console.log('  - portfolios (added agent_id column)');

    // Verify tables exist
    const tables = database.prepare(`
      SELECT name FROM sqlite_master
      WHERE type='table' AND name IN (
        'trading_agents',
        'agent_signals',
        'agent_portfolios',
        'agent_activity_log'
      )
      ORDER BY name
    `).all();

    console.log('');
    console.log('Verified tables:', tables.map(t => t.name).join(', '));

  } catch (error) {
    database.exec('ROLLBACK');
    console.error('Migration failed:', error.message);
    throw error;
  }
}

// Helper function to check if migration has been run
function isMigrationNeeded() {
  const database = db.getDatabase();
  const result = database.prepare(`
    SELECT COUNT(*) as count FROM sqlite_master
    WHERE type='table' AND name='trading_agents'
  `).get();
  return result.count === 0;
}

// Run migration if executed directly
if (require.main === module) {
  if (isMigrationNeeded()) {
    runMigration();
  } else {
    console.log('Trading agents entity tables already exist. Migration skipped.');
  }
}

module.exports = { runMigration, isMigrationNeeded };
